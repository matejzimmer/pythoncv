def volny_pad(vyska, gravitace = EARTH_GRAVITY):
    cas = (2*vyska / gravitace) ** 0.5
    return cas

def relat_mass(mass, velocity):
    relat_mass = mass / (1 - (velocity ** 2 / SPEED_OF_LIGHT ** 2)) ** 0.5
    return relat_mass

def mach_number(airspeed, speed = SPEED_OF_SOUND):
    mach_number= airspeed / speed
    return mach_number